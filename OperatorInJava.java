public class OperatorInJava {
								//Operator are 4 type 1. Arithmetic 2. Logical 3. bitwise 4. Relational
								// Here we use Arithmetic Operator, relational operator use in if else structure and 
								//
	public static void main(String[] args) {
		int a = 12,b = 7;
		
		int r1 = a+b;
		int r2 = a-b;
		int r3 = a*b;
		double r4 = (double) a/b;
		int r5 = a%b;
		System.out.println(r1);
		System.out.println(r2);
		System.out.println(r3);
		System.out.println(r4);
		System.out.println(r5);
		
	}

}
